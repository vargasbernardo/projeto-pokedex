export default function getCardBackground(type) {
    if (type === 'bug') {
        return '#76A866'
    } else if (type === 'fire') {
        return '#EAAB7D'
    } else if (type === 'grass') {
        return '#729F92'
    } else if (type === 'water') {
        return '#71C3FF'
    } else if (type === 'normal') {
        return '#BF9762'
    } else if (type === 'dragon') {
        return '#004170'
    } else if (type === 'dark') {
        return '#36303B'
    } else if (type === 'electric') {
        return '#F8E383'
    } else if (type === 'fairy') {
        return '#F0A5EB'
    } else if (type === 'fighting') {
        return '#D45479'
    } else if (type === 'ghost') {
        return '#6278B5'
    } else if (type === 'ground') {
        return '#70351A'
    } else if (type === 'ice') {
        return '#3BA091'
    } else if (type === 'psychic') {
        return '#F8A0A4'
    } else if (type === 'rock') {
        return '#7A6A3F'
    } else if (type === 'steel') {
        return '#7B7B7B'
    } else if (type === 'poison') {
        return '#5D325D'
    }
}