export default function getTypeBackground(type) {
    if (type === 'bug') {
        return '#316520'
    } else if (type === 'fire') {
        return '#F44900'
    } else if (type === 'flying') {
        return '#6892B0'
    } else if (type === 'grass') {
        return '#70B873'
    } else if (type === 'normal') {
        return '#8A8A8A'
    } else if (type === 'poison') {
        return '#AD61AE'
    } else if (type === 'water') {
        return '#33A4F5'
    } else if (type === 'dragon') {
        return '#0A6CBF'
    } else if (type === 'dark') {
        return '#5C5365'
    } else if (type === 'electric') {
        return '#F4D23B'
    } else if (type === 'fairy') {
        return '#EC8FE6'
    } else if (type === 'fighting') {
        return '#CE4069'
    } else if (type === 'ghost') {
        return '#5269AC'
    } else if (type === 'ground') {
        return '#D97745'
    } else if (type === 'ice') {
        return '#74CEC0'
    } else if (type === 'psychic') {
        return '#F67176'
    } else if (type === 'rock') {
        return '#C7B78B'
    } else if (type === 'steel') {
        return '#BBBBBB'
    }
}